# include "3-calc.h"
# include <stdio.h>
# include <stdlib.h>
/**
 * op_add - op add
 * @a: number
 * @b: number
 * Return: Always add (Success)
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - op sub
 * @a: number
 * @b: number
 * Return: Always sub (Success)
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - op mul
 * @a: number
 * @b: number
 * Return: Always mul (Success)
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - op div
 * @a: number
 * @b: number
 * Return: Always div (Success)
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - op mod
 * @a: number
 * @b: number
 * Return: Always mod (Success)
*/
int op_mod(int a, int b)
{
	return (a % b);
}
